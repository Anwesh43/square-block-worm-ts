const w : number = window.innerWidth
const h : number = window.innerHeight
const nodes : number = 5
const squares : number = 2
const scGap : number = 0.05
const scDiv : number = 0.51
const strokeFactor : number = 90
const sizeFactor : number = 1
const rSizeFactor : number = 3
const foreColor : string = "#4CAF50"
const backColor : string = "#BDBDBD"

const maxScale : Function = (scale : number, i : number, n : number) : number => {
    return Math.max(0, scale - i / n)
}

const divideScale : Function = (scale : number, i : number, n : number) : number => {
    return Math.min(1 / n, maxScale(scale, i, n)) * n
}

const scaleFactor : Function = (scale : number) : number => Math.floor(scale / scDiv)

const mirrorValue : Function = (scale : number, a : number, b : number) => {
    const k : number = scaleFactor(scale)
    return (1 - k) / a + k / b
}

const updateValue : Function = (scale : number, dir : number, a : number, b : number) : number => {
    return mirrorValue(scale, a, b) * dir * scGap
}

const drawSBWNode : Function = (context : CanvasRenderingContext2D, i : number, scale : number) => {
    const gap : number = h / (nodes + 1)
    const size : number = gap / sizeFactor
    const rSize : number = size / rSizeFactor
    context.strokeStyle = foreColor
    context.fillStyle = foreColor
    context.lineWidth = Math.min(w, h) / strokeFactor
    context.lineCap = 'round'
    context.save()
    context.translate(w / 2, gap * (i + 1))
    for (var j = 0; j < squares; j++) {
        const sc : number = divideScale(scale, j, squares)
        context.save()
        context.translate(0, size * sc)

        context.restore()
    }
    context.beginPath()
    context.moveTo(0, size * divideScale(scale, 1, 2))
    context.lineTo(0, size * divideScale(scale, 0, 2))
    context.stroke()
    context.restore()
}

